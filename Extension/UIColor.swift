import UIKit

extension UIColor {
    class func customBlue() -> UIColor {
        return UIColor(red: 0/255, green: 0/255, blue: 92/255, alpha: 1)
    }
}

extension UIColor {
    class func customPurple() -> UIColor {
        return UIColor(red: 106/255, green: 9/255, blue: 125/255, alpha: 1)
    }
}

extension UIColor {
    class func customPink() -> UIColor {
        return UIColor(red: 192/255, green: 96/255, blue: 161/255, alpha: 1)
    }
}

extension UIColor {
    class func customYellow() -> UIColor {
        return UIColor(red: 255/255, green: 220/255, blue: 180/255, alpha: 1)
    }
}
